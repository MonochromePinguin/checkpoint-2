<?php

namespace Model;

/**
 * Class Movie
 */
class Movie
{
    private $id;
    private $title;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getTitle(): string
    {
        return $this->title;
    }
}
